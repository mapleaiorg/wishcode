/**
 * iBank UI string catalogue — 10 languages.
 *
 * Keys are dot-namespaced by surface:
 *   - common.*     reusable words / actions
 *   - sidebar.*    Sidebar component
 *   - chat.*       ChatView, welcome, composer
 *   - title.*      Window title bar / chat title bar
 *   - trade.*      TradingPanel
 *   - login.*      LoginModel
 *   - model.*      ModelPicker
 *   - usermenu.*   UserMenu popover
 *
 * English is the source of truth and the fallback for every other
 * locale. Translators deliver only keys they can translate; missing
 * keys transparently fall back to English at lookup time.
 *
 * No RTL locales are shipped at the moment (Arabic and Russian were
 * dropped; Traditional Chinese Big5 was added).
 */

export type Lang =
  | 'en' | 'zh' | 'zh-TW' | 'ja' | 'ko'
  | 'es' | 'fr' | 'de' | 'pt'

export interface LangMeta {
  code: Lang
  label: string            // Native label ("简体中文", "日本語", …)
  english: string          // English label for accessibility / search
  dir?: 'ltr' | 'rtl'
}

